public class Stats {
    /** Variables **/
    private int hour_count = 0;
    private int day_count = 0;
    private int week_count = 0;
    private int total_count = 0;


    /*** Getter and setter **/
    public int getHourCount() {
        return hour_count;
    }

    public void setHourCount(int hour_count) {
        this.hour_count = hour_count;
    }

    public int getDayCount() {
        return day_count;
    }

    public void setDayCount(int day_count) {
        this.day_count = day_count;
    }

    public int getWeekCount() {
        return week_count;
    }

    public void setWeekCount(int week_count) {
        this.week_count = week_count;
    }

    public int getTotalCount() {
        return total_count;
    }

    public void setTotalCount(int total_count) {
        this.total_count = total_count;
    }


}
